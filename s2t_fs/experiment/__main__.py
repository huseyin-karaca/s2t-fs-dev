"""
Unified Hydra entry point for all S2T-FS experiments.

Usage
-----
Compose at the CLI:
    python -m s2t_fs.experiment                           # defaults (multi, ami, full_benchmark)
    python -m s2t_fs.experiment mode=single model_group=fastt_boosted_only
    python -m s2t_fs.experiment data=voxpopuli search=quick

Load a named, frozen snapshot (the "second-level" reproducible config):
    python -m s2t_fs.experiment +experiment=ami_baseline
    python -m s2t_fs.experiment +experiment=ami_margin_v1

Mode semantics
--------------
- ``mode=single``  → picks the first model in ``models`` and runs HPT on it.
- ``mode=multi``   → runs the multi-model comparison on all models.
- ``mode=margin``  → runs the Optuna margin-optimization study over list-valued
                     data/search params (the same mechanism as before).
"""

import os
from datetime import datetime
from pathlib import Path

import hydra
import mlflow
import yaml
from omegaconf import DictConfig, OmegaConf

from s2t_fs.experiment.train_margin_optimization import margin_optimization_experiment
from s2t_fs.experiment.train_multi_model import multi_model_experiment
from s2t_fs.experiment.train_single_model import hpt_single_model
from s2t_fs.utils.logger import custom_logger as logger
from s2t_fs.utils.torch_utils import log_hardware_info

# Path to the configs/ directory at the repo root (two levels up from this file).
_REPO_ROOT = Path(__file__).resolve().parents[2]
_CONFIG_DIR = str(_REPO_ROOT / "configs")


def _write_margin_best_snapshot(result, cfg_dict, choices):
    """Auto-promote the winning margin-optimization trial into a reproducible
    ``configs/experiment/<dataset>_margin_best.yaml`` snapshot.

    The snapshot is in ``multi`` mode with the winning scalar values baked in,
    so re-running ``+experiment=<dataset>_margin_best`` reproduces the winning
    comparison exactly.

    The file is overwritten each run. Rename it (e.g. to ``ami_margin_v2.yaml``)
    to preserve a snapshot you care about.
    """
    best_params = result["best_params"]  # keys like "data.row_subsample", "search.seed"
    best_margin = result["best_margin"]
    trial_num = result["best_trial_number"]
    parent_run_id = result["parent_run_id"]

    data_choice = choices.get("data", "unknown")
    model_group_choice = choices.get("model_group", "full_benchmark")

    # Resolve winning values: start from the scalar fields in the current cfg,
    # then overlay Optuna's picks.
    data_overrides = {}
    search_overrides = {}
    for key, value in best_params.items():
        if key.startswith("data."):
            data_overrides[key[len("data.") :]] = value
        elif key.startswith("search."):
            search_overrides[key[len("search.") :]] = value

    snapshot = {
        "defaults": [
            {"override /mode": "multi"},
            {"override /data": data_choice},
            {"override /model_group": model_group_choice},
            "_self_",
        ],
    }
    if data_overrides:
        snapshot["data"] = data_overrides
    if search_overrides:
        snapshot["search"] = search_overrides

    header = (
        "# @package _global_\n"
        "#\n"
        "# AUTO-GENERATED by margin optimization. Overwritten on every margin run.\n"
        f"# Generated: {datetime.now().isoformat(timespec='seconds')}\n"
        f"# Source study: dataset={data_choice}, model_group={model_group_choice}\n"
        f"# Best trial:   #{trial_num}   margin = {best_margin:+.4f}\n"
        f"# MLflow parent run: {parent_run_id}\n"
        "#\n"
        "# To preserve this snapshot, rename the file (e.g. ami_margin_v2.yaml).\n"
        "# To rerun: python -m s2t_fs.experiment +experiment="
        f"{data_choice}_margin_best\n"
        "\n"
    )

    out_path = _REPO_ROOT / "configs" / "experiment" / f"{data_choice}_margin_best.yaml"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with open(out_path, "w") as f:
        f.write(header)
        yaml.safe_dump(snapshot, f, sort_keys=False, default_flow_style=False)

    logger.bind(category="Margin-Opt").success(
        f"Auto-promoted winning trial to {out_path.relative_to(_REPO_ROOT)}"
    )


@hydra.main(version_base=None, config_path=_CONFIG_DIR, config_name="config")
def main(cfg: DictConfig) -> None:
    # Resolve to a plain dict — the train_*.py functions expect dicts, not DictConfigs.
    cfg_dict = OmegaConf.to_container(cfg, resolve=True)

    mode = cfg_dict["mode"]["name"]
    mlflow_params = cfg_dict["mlflow"]
    data_params = cfg_dict["data"]
    search_params = cfg_dict["search"]
    evaluation_params = cfg_dict["evaluation"]
    # model_group files mount each model under model_group.models.<Name>,
    # so that's the canonical location. Fall back to top-level `models` for
    # experiment snapshots that define them inline.
    models = cfg_dict.get("model_group", {}).get("models") or cfg_dict.get("models", {})
    callbacks_config = cfg_dict.get("callbacks")

    if not models:
        raise ValueError(
            "No models in config. Select a model_group, e.g. `model_group=full_benchmark`."
        )

    log_hardware_info(logger)

    # Inject mlflow_params into nested calls that still read them by name.
    # (train_margin_optimization reads cfg['mlflow_params'] directly.)
    full_cfg_compat = {
        "mlflow_params": mlflow_params,
        "data_params": data_params,
        "search_params": search_params,
        "evaluation_params": evaluation_params,
        "models": models,
    }
    if "optimization_params" in cfg_dict:
        full_cfg_compat["optimization_params"] = cfg_dict["optimization_params"]
    if callbacks_config is not None:
        full_cfg_compat["callbacks"] = callbacks_config

    # MLflow setup (skipped when running under `mlflow run`, which sets MLFLOW_RUN_ID).
    if "MLFLOW_RUN_ID" not in os.environ:
        mlflow.set_tracking_uri(mlflow_params["tracking_uri"])
        mlflow.set_experiment(mlflow_params["experiment_name"])

    # Hydra writes the fully resolved config here; we pass it as "config_path"
    # so the existing artifact-logging code can attach it to MLflow runs.
    resolved_config_path = str(
        Path(hydra.core.hydra_config.HydraConfig.get().runtime.output_dir)
        / ".hydra"
        / "config.yaml"
    )

    if mode == "margin":
        result = margin_optimization_experiment(
            full_cfg_compat, config_path=resolved_config_path
        )
        if result is not None:
            choices = dict(hydra.core.hydra_config.HydraConfig.get().runtime.choices)
            _write_margin_best_snapshot(result, cfg_dict, choices)

    elif mode == "multi":
        multi_model_experiment(
            models=models,
            data_params=data_params,
            search_params=search_params,
            evaluation_params=evaluation_params,
            mlflow_params=mlflow_params,
            callbacks_config=callbacks_config,
            config_path=resolved_config_path,
        )

    elif mode == "single":
        model_name = next(iter(models))
        model_cfg = models[model_name]
        hpt_single_model(
            model_name=model_name,
            model_cfg=model_cfg,
            data_params=data_params,
            search_params=search_params,
            callbacks_config=callbacks_config,
            config_path=resolved_config_path,
        )

    else:
        raise ValueError(f"Unknown mode: {mode!r}. Expected one of single/multi/margin.")


if __name__ == "__main__":
    main()
